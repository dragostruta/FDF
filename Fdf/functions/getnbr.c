int		getnbr(char *s)
{
	int		i;
	int		n;
	int		sign;

	i = 0;
	n = 0;
	sign = 1;
	if (s[0] == '-')
		sign = -1;
	while (s[i] != '\0' && s[i] != '\n')
	{
		if (s[i] >= '0' && s[i] <= '9')
			n = n * 10 + ((int)(s[i]) - (int)('0'));
		i++;
	}
	return (n * sign);
}
